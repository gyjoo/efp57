fnum = eval(input("What is the first number? "))
snum = eval(input("What is the second number? "))

plus = fnum + snum
minus = fnum - snum
multiply = fnum * snum
divide = fnum / snum

print(fnum, "+" , snum , "=", plus)
print(fnum, "-" , snum , "=", minus)
print(fnum, "*" , snum , "=", multiply)
print(fnum, "/" , snum , "= %d"%divide)
